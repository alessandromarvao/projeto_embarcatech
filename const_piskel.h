// Função para converter hexadecimal para RGB
void hexToRGB(unsigned int hex, int rgb[3])
{
    // Extrai os componentes RGB, ignorando o alfa (últimos 8 bits)
    // Extrai os 8 bits do vermelho
    rgb[0] = hex & 0xFF;
    // Extrai os 8 bits do verde
    rgb[1] = (hex >> 8) & 0xFF;
    // Extrai os 8 bits do azul
    rgb[2] = (hex >> 16) & 0xFF;
}

/**
 * Armazena os dados das cores hexadecimais convertidas em RGB na array recebida por parâmetro
 * @param array Matriz que receberá os valores das cores do sprite gerado (são 144 vetores contendo 25 vetores, que contém um vetor com 3 elementos)
 */
void convert_array(int array[144][25][3])
{
    // Percorre todos os 144 elementos de sprites gerados
    for (int i = 0; i < 144; i++)
    {
        // Percorre toda a matriz de 25 elementos de cada sprite gerado
        for (int j = 0; j < 25; j++)
        {
            // Passa o array rgb[3] para a função hexToRGB
            hexToRGB(new_piskel_data[i][j], array[i][j]);
        }
    }

}

